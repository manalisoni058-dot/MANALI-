# Simple Food Ordering App

# Fruit menu with prices
menu = {
    "Apple": 30,
    "Banana": 10,
    "Grapes": 50,
    "Mango": 40,
    "Orange": 20,
    "Pineapple": 60,
    "Papaya": 35,
    "Watermelon": 80,
    "Strawberry": 90,
    "Blueberry": 120,
    "Kiwi": 45,
    "Pomegranate": 70,
    "Guava": 25,
    "Peach": 55,
    "Cherry": 110
}

print("===== Welcome to the Fruit Ordering App =====")
print("\nAvailable Fruits and Prices:")
for fruit, price in menu.items():
    print(f"{fruit} - Rs {price}")

cart = {}  # to store selected items and quantities

while True:
    choice = input("\nEnter a fruit name to add to cart (or type 'done' to finish): ").title()

    if choice == "Done":
        break

    # Check if fruit exists
    if choice in menu:
        qty = int(input(f"Enter quantity of {choice}: "))
        if choice in cart:
            cart[choice] += qty
        else:
            cart[choice] = qty
        print(f"{qty} {choice}(s) added to cart!")

    else:
        print("Invalid fruit! Please select from the menu.")

# ------- BILL GENERATION -------
print("\n========== FINAL BILL ==========")
total = 0

for fruit, qty in cart.items():
    price = menu[fruit]
    cost = price * qty
    total += cost
    print(f"{fruit} √ó {qty} = Rs {cost}")

print("--------------------------------")
print(f"Total Amount: Rs {total}")
print("================================")
print("Thank you for your order!")

